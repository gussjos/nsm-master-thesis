function K = hindranceFactor(lambda)

%taken from Panadda Dechadilok and William M. Deen: Hindrance Factors for Diffusion and Convection in Pores

%cylindrical 
H = 1+9/8.*lambda.*log(lambda)...
    -1.56034*lambda ...
    + 0.528155*lambda.^2 ...
    + 1.91521*lambda.^3 ...
    - 2.81903*lambda.^4 ...
    + 0.270788*lambda.^5 ...
    + 1.10115*lambda.^6 ...
    - 0.435933*lambda.^7; 

K = H./(1-lambda).^2;